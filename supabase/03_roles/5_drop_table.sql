-- =====================================================
-- 📋 محتويات الملف
-- =====================================================
-- 1. حذف السياسات (Policies)
-- 2. حذف الدوال (Functions)
-- 3. حذف الفهارس (Indexes)
-- 4. حذف البيانات (Data)
-- 5. حذف الجدول (Table)
-- 6. حذف النوع (ENUM Type)
-- =====================================================


-- =====================================================
-- 1️⃣ حذف السياسات (Policies)
-- =====================================================

DROP POLICY IF EXISTS "roles_admin_manage" ON public.roles;
DROP POLICY IF EXISTS "roles_public_read" ON public.roles;


-- =====================================================
-- 2️⃣ حذف الدوال (Functions)
-- =====================================================
-- لا توجد دوال لهذا الجدول


-- =====================================================
-- 3️⃣ حذف الفهارس (Indexes)
-- =====================================================

DROP INDEX IF EXISTS idx_roles_created_at;
DROP INDEX IF EXISTS idx_roles_permissions;
DROP INDEX IF EXISTS idx_roles_name;


-- =====================================================
-- 4️⃣ حذف البيانات (Data)
-- =====================================================

DELETE FROM public.roles;


-- =====================================================
-- 5️⃣ حذف الجدول (Table)
-- =====================================================

DROP TABLE IF EXISTS public.roles CASCADE;


-- =====================================================
-- 6️⃣ حذف النوع (ENUM Type)
-- =====================================================

DROP TYPE IF EXISTS public.role_name CASCADE;


